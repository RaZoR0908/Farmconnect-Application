// Helper to get product name in selected language
export const getProductName = (englishName, language) => {
  return productNames[englishName]?.[language] || englishName;
};

// Helper to get English name from translated name
export const getEnglishProductName = (translatedName, language) => {
  if (language === 'English') return translatedName;
  
  for (const [englishName, translations] of Object.entries(productNames)) {
    if (translations[language] === translatedName) {
      return englishName;
    }
  }
  return translatedName; // Return as-is if not found
};
